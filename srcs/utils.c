int	is_numeric(char *str)
{
	int	i;

	i = 0;
	if (*str == '-')
		str++;
	if (*str == '\0')
		return (0);
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	return (1);
}